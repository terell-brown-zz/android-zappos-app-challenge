public class ClearAllLink {


    private String url;

    private String text;

    private Object type;

    /**
     * @return The url
     */
    public String getUrl() {
        return url;
    }

    /**
     * @param url The url
     */
    public void setUrl(String url) {
        this.url = url;
    }

    /**
     * @return The text
     */
    public String getText() {
        return text;
    }

    /**
     * @param text The text
     */
    public void setText(String text) {
        this.text = text;
    }

    /**
     * @return The type
     */
    public Object getType() {
        return type;
    }

    /**
     * @param type The type
     */
    public void setType(Object type) {
        this.type = type;
    }

}
